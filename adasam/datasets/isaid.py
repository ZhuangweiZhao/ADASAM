"""
iSAID 实例小样本数据集 | iSAID Instance Few-Shot Dataset.
========================================================

基于 896×896 tile + COCO JSON 的实例分割数据集, 支持 Base/Novel 分划。
Instance-segmentation dataset over 896×896 tiles + COCO JSON, with Base/Novel split.

数据由 AdaTile-FastSAM 的 ``prep_isaid_instance.py`` 预生成, 本仓库仅按路径引用 (见
configs/base.yaml:data_root), 不复制。契约与该预处理输出逐字一致。
Data is pre-generated by AdaTile-FastSAM's prep_isaid_instance.py; this repo references it
by path (configs/base.yaml:data_root) rather than copying. The contract matches that output.

样本契约 | Sample contract::

    {
        "image":         Tensor[3, 896, 896] float32, RGB, ∈ [0, 1],
        "instances":     [ {"mask": Tensor[H,W] bool, "category_id": int,
                            "bbox": [x,y,w,h], "area": float}, ... ],
        "tile_id":       int,   # == COCO image_id (评估对齐键 | eval join key)
        "orig_image_id": int,   # 源全图 id (场景不相交采样用 | for scene-disjoint sampling)
        "tile_origin":   (x, y),
    }

图像保持 896² [0,1]; MobileSAM 的 1024² 归一化在 adasam.utils.transforms 完成 (SRP)。
Images stay 896² [0,1]; MobileSAM's 1024² normalization happens in adasam.utils.transforms.
"""

from __future__ import annotations

import gc
import json
from collections import defaultdict
from pathlib import Path

import cv2
import numpy as np
import torch
from torch.utils.data import Dataset

from adasam.logging import get_logger

logger = get_logger("dataset.isaid")

# ═══════════════════════════════════════════════════════════════════
# 常量 | Constants
# ═══════════════════════════════════════════════════════════════════

ISAID_CATEGORIES: dict[int, str] = {
    1: "small_vehicle", 2: "large_vehicle", 3: "plane",
    4: "storage_tank", 5: "ship", 6: "harbor",
    7: "ground_track_field", 8: "soccer_ball_field", 9: "tennis_court",
    10: "swimming_pool", 11: "baseball_diamond", 12: "basketball_court",
    13: "bridge", 14: "helicopter", 15: "roundabout",
}

# 3-Fold Base/Novel 划分, 与 prep_isaid_instance.py 一致 | consistent with prep pipeline
DEFAULT_FOLDS: dict[int, dict[str, list[int]]] = {
    0: {"base": [5, 4, 11, 9, 7, 13, 2, 14, 8, 3], "novel": [1, 6, 10, 12, 15]},
    1: {"base": [5, 4, 11, 12, 1, 14, 10, 15, 8, 6], "novel": [9, 7, 13, 2, 3]},
    2: {"base": [9, 12, 7, 13, 2, 1, 10, 15, 3, 6], "novel": [5, 4, 11, 14, 8]},
}

MIN_INSTANCE_AREA = 16    # 小于此像素面积的实例丢弃 | drop instances below this pixel area


# ═══════════════════════════════════════════════════════════════════
# 数据集 | Dataset
# ═══════════════════════════════════════════════════════════════════

class ISAIDInstanceDataset(Dataset):
    """iSAID 实例小样本数据集 | iSAID instance few-shot dataset.

    :param root: 数据根目录 | data root (contains annotations/, images/, folds/).
    :param split: "train" 或 "val" | split.
    :param fold: Fold ID (0/1/2), 决定 Base/Novel 类别划分 | determines the split.
    :param mode: "base" | "novel" | "all" —— 决定可见类 | visible classes.
    :param base_ids / novel_ids: 手动覆盖 fold 定义 | override fold definitions.
    :param min_instances_per_tile: 仅保留实例数 ≥ 此值的 tile | keep tiles with ≥ N instances.
    """

    def __init__(
        self,
        root: str | Path,
        split: str = "train",
        fold: int = 0,
        mode: str = "base",
        base_ids: list[int] | None = None,
        novel_ids: list[int] | None = None,
        min_instances_per_tile: int = 0,
        ann_file: str | Path | None = None,
    ) -> None:
        if mode not in ("base", "novel", "all"):
            raise ValueError(f"mode must be 'base'|'novel'|'all', got '{mode}'")

        self.root = Path(root)
        self.split = split
        self.fold = fold
        self.mode = mode

        self.base_ids, self.novel_ids = self._resolve_split(fold, base_ids, novel_ids)
        if mode == "base":
            self._visible_ids = set(self.base_ids)
        elif mode == "novel":
            self._visible_ids = set(self.novel_ids)
        else:
            self._visible_ids = set(self.base_ids) | set(self.novel_ids)

        # ── 加载 COCO JSON | Load COCO JSON ──
        # ann_file 覆盖时使用外部标注 (如 RAM 精简子集); 图像/fold 仍取自 root。
        # When ann_file is given, use an external annotation file (e.g. a RAM-lean subset);
        # images/folds still come from root.
        anno_path = Path(ann_file) if ann_file else (
            self.root / "annotations" / f"instances_{split}.json")
        if not anno_path.exists():
            raise FileNotFoundError(
                f"Annotation file not found: {anno_path}. "
                f"Point configs/base.yaml:data_root at the prepared iSAID split."
            )
        with open(anno_path, encoding="utf-8") as f:
            coco = json.load(f)

        self._tiles: list[dict] = coco.get("images", [])
        self._img_dir = self.root / "images" / split

        # ── 建索引: tile→(可见类)标注, class→tile, 激活 tile 列表 ──
        # Build indices: tile→(visible) anns, class→tiles, active tile list.
        self._ann_by_tile: dict[int, list[dict]] = defaultdict(list)
        for ann in coco.get("annotations", []):
            if ann.get("category_id", 0) in self._visible_ids:
                self._ann_by_tile[ann["image_id"]].append(ann)

        self._active: list[int] = []
        self._class_to_tiles: dict[int, list[int]] = defaultdict(list)
        for tile_idx, tile in enumerate(self._tiles):
            anns = self._ann_by_tile.get(tile["id"], [])
            if min_instances_per_tile > 0 and len(anns) < min_instances_per_tile:
                continue
            self._active.append(tile_idx)
            for cat_id in {a["category_id"] for a in anns}:
                self._class_to_tiles[cat_id].append(tile_idx)

        n_anns = sum(len(self._ann_by_tile.get(self._tiles[i]["id"], [])) for i in self._active)
        del coco                 # 释放原始 JSON 字典 (非可见类标注就此回收) | free raw parse
        gc.collect()
        logger.log_info(
            "load",
            f"{split}/fold{fold}/{mode}: {len(self._active)} tiles, {n_anns} instances, "
            f"classes={sorted(self._visible_ids)}",
        )

    # ── 类别划分 | Class split resolution ──

    def _resolve_split(
        self, fold: int, base_ids: list[int] | None, novel_ids: list[int] | None
    ) -> tuple[list[int], list[int]]:
        """从 folds/ JSON 或 DEFAULT_FOLDS 解析 base/novel | resolve base/novel from disk or default."""
        fold_path = self.root / "folds" / f"fold_{fold}.json"
        if fold_path.exists():
            with open(fold_path, encoding="utf-8") as f:
                fd = json.load(f)
        elif fold in DEFAULT_FOLDS:
            fd = DEFAULT_FOLDS[fold]
        else:
            raise ValueError(f"Unknown fold {fold}; available {list(DEFAULT_FOLDS)}")
        return (base_ids or list(fd["base"]), novel_ids or list(fd["novel"]))

    # ── 核心接口 | Core interface ──

    def __len__(self) -> int:
        return len(self._active)

    def __getitem__(self, idx: int) -> dict:
        tile = self._tiles[self._active[idx]]
        h, w = int(tile["height"]), int(tile["width"])

        img_path = self._img_dir / tile["file_name"]
        bgr = cv2.imread(str(img_path), cv2.IMREAD_COLOR)
        if bgr is None:
            raise FileNotFoundError(f"Cannot read tile image: {img_path}")
        rgb = cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB).astype(np.float32) / 255.0
        image = torch.from_numpy(rgb).permute(2, 0, 1).contiguous()   # [3, H, W] ∈ [0,1]

        instances = []
        for ann in self._ann_by_tile.get(tile["id"], []):
            mask = self._render_mask(ann, h, w)
            area = int(mask.sum())
            if area < MIN_INSTANCE_AREA:
                continue
            instances.append({
                "mask": torch.from_numpy(mask > 0).bool(),
                "category_id": int(ann["category_id"]),
                "bbox": [float(v) for v in ann.get("bbox", [0, 0, 0, 0])],
                "area": float(area),
            })

        return {
            "image": image,
            "instances": instances,
            "tile_id": int(tile["id"]),
            "orig_image_id": int(tile.get("orig_image_id", tile["id"])),
            "tile_origin": (int(tile.get("orig_x", 0)), int(tile.get("orig_y", 0))),
        }

    @staticmethod
    def _render_mask(ann: dict, h: int, w: int) -> np.ndarray:
        """渲染单实例二值掩码 (polygon, 回退 bbox) | Render a single-instance binary mask."""
        mask = np.zeros((h, w), dtype=np.uint8)
        seg = ann.get("segmentation", [])
        if seg and isinstance(seg, list):
            if isinstance(seg[0], list):
                polys = seg
            elif isinstance(seg[0], (int, float)):
                polys = [seg]
            else:
                polys = []
            for poly in polys:
                if len(poly) < 6:
                    continue
                pts = np.array(poly, dtype=np.int32).reshape(-1, 1, 2)
                pts[:, :, 0] = np.clip(pts[:, :, 0], 0, w - 1)
                pts[:, :, 1] = np.clip(pts[:, :, 1], 0, h - 1)
                cv2.fillPoly(mask, [pts], 1)
        else:
            x, y, bw, bh = (max(0, int(v)) for v in ann.get("bbox", [0, 0, 0, 0]))
            bw, bh = min(bw, w - x), min(bh, h - y)
            if bw > 0 and bh > 0:
                mask[y:y + bh, x:x + bw] = 1
        return mask

    # ── 查询接口 (供 episode 采样) | Query interface (for episode sampling) ──

    def class_to_tiles(self, class_id: int) -> list[int]:
        """含指定类的 tile 索引 (数据集索引空间) | tile indices containing a class (dataset-index space)."""
        return list(self._class_to_tiles.get(class_id, []))

    def visible_classes(self) -> list[int]:
        """当前 mode 下可见类 ID | visible class IDs under current mode."""
        return sorted(self._visible_ids)

    def source_image_id(self, idx: int) -> int:
        """tile 的源全图 id (场景不相交判据) | the tile's source-scene id (scene-disjoint key)."""
        return int(self._tiles[self._active[idx]].get("orig_image_id", self._active[idx]))
